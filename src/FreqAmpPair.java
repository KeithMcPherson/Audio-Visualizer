
public class FreqAmpPair {
	double frequency;
	double amplitude;
	
	public FreqAmpPair(double frequency, double amplitude) {
		this.frequency = frequency;
		this.amplitude = amplitude;
	}
	
	public double getAmplitude(){
		return amplitude;
	}
	
	public double getFrequency(){
		return frequency;
	}
}
